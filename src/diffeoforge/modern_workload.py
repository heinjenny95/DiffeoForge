def _is_generated_report(directory: Path) -> bool:
    if not directory.is_dir():
        return False
    if {path.name for path in directory.iterdir()} != {REPORT_JSON_NAME, REPORT_HTML_NAME}:
        return False
    try:
        report = json.loads((directory / REPORT_JSON_NAME).read_text(encoding="utf-8"))
        first_line = (directory / REPORT_HTML_NAME).read_text(encoding="utf-8").splitlines()[0]
    except (OSError, UnicodeError, json.JSONDecodeError, IndexError):
        return False
    return report.get("workload_version") == WORKLOAD_VERSION and first_line == HTML_MARKER


def write_modern_workload_report(
    report: dict[str, Any],
    destination: Path | str,
    *,
    overwrite: bool = False,
) -> Path:
    """Atomically publish deterministic JSON and HTML in a dedicated directory."""

    output = Path(destination).expanduser().resolve()
    _validate_report(report)
    if output.exists() and not overwrite:
        raise FileExistsError(f"Modern workload report already exists: {output}")
    if output.exists() and not _is_generated_report(output):
        raise ModernWorkloadError(
            f"Refusing to replace a directory not recognized as a generated report: {output}"
        )
    output.parent.mkdir(parents=True, exist_ok=True)
    temporary = output.parent / f".{output.name}.tmp-{uuid.uuid4().hex}"
    backup = output.parent / f".{output.name}.backup-{uuid.uuid4().hex}"
    temporary.mkdir()
    try:
        with (temporary / REPORT_JSON_NAME).open(
            "x", encoding="utf-8", newline="\n"
        ) as handle:
            json.dump(report, handle, indent=2, ensure_ascii=False, sort_keys=True)
            handle.write("\n")
        (temporary / REPORT_HTML_NAME).write_text(
            render_modern_workload_html(report),
            encoding="utf-8",
            newline="\n",
        )
        if output.exists():
            output.rename(backup)
        try:
            temporary.rename(output)
        except Exception:
            if backup.exists() and not output.exists():
                backup.rename(output)
            raise
        if backup.exists():
            # Publishing already succeeded. A transient cleanup problem must not
            # report the newly visible, valid output as a failed publication.
            shutil.rmtree(backup, ignore_errors=True)
    except Exception:
        if temporary.exists():
            shutil.rmtree(temporary)
        raise
    return output


def default_modern_workload_path(config_path: Path | str) -> Path:
    source = Path(config_path).expanduser().resolve()
    return source.with_suffix(".workload")


def plan_modern_workload(
    config_path: Path | str,
    *,
    destination: Path | str | None = None,
    overwrite: bool = False,
) -> Path:
    """Collect and atomically publish one pre-compute workload plan."""

    report = collect_modern_workload(config_path)
    output = (
        default_modern_workload_path(config_path) if destination is None else destination
    )
    return write_modern_workload_report(report, output, overwrite=overwrite)
