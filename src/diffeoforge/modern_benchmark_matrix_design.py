from __future__ import annotations

import hashlib
import html
import json
import shutil
import uuid
from collections.abc import Sequence
from datetime import UTC, datetime
from importlib.resources import files
from pathlib import Path
from typing import Any

import jsonschema

from diffeoforge import __version__
from diffeoforge.config import ConfigurationError, validate_input_paths
from diffeoforge.mesh import inspect_inputs, sha256_file
from diffeoforge.modern_benchmark import TILE_OVERRIDE_BENCHMARK_VERSION
from diffeoforge.modern_workflow import (
    load_modern_workflow_config,
    pairwise_evaluation_from_config,
)

MATRIX_DESIGN_VERSION = "0.1"
MATRIX_DESIGN_JSON_NAME = "matrix-design.json"
MATRIX_DESIGN_SIDECAR_NAME = "matrix-design.sha256"
MATRIX_DESIGN_HTML_NAME = "matrix-design.html"
HTML_MARKER = (
    "<!-- Generated by DiffeoForge modern-benchmark-matrix-design; "
    "immutable pre-results evidence. -->"
)
ORDER_ALGORITHM = "sha256-ranked-subject-tile-cells-and-within-cell-strategies-v0.1"
CONFIG_PLACEHOLDER = "<verified-source-config>"
STRATEGIES = ("standard", "recompute")
MAX_SUBJECT_COUNT = 100_000
MAX_TILE_DIMENSION = 999_999
MAX_MATRIX_CONDITIONS = 1_000
ANALYSIS_POLICY = (
    "Preserve separate raw reports; do not automatically select a winner, calculate speedup or "
    "significance, choose a preset, or extrapolate to a full workflow or larger cohort."
)
SCIENTIFIC_BOUNDARY = (
    "This artifact freezes a full-factorial subject-prefix, tile-shape, and autograd-strategy "
    "benchmark design before results exist. It performs no benchmark, statistical analysis, "
    "peak-memory measurement, full-workflow measurement, safe-preset selection, scaling "
    "inference, or 300-subject feasibility test."
)


class ModernBenchmarkMatrixDesignError(RuntimeError):
    """Raised when a prospective matrix design is invalid or cannot be published."""


def _schema() -> dict[str, Any]:
    resource = files("diffeoforge.schema").joinpath(
        "modern-benchmark-matrix-design-v0.1.json"
    )
    return json.loads(resource.read_text(encoding="utf-8"))


def _format_schema_error(error: jsonschema.ValidationError) -> str:
    location = ".".join(str(part) for part in error.absolute_path) or "document"
    return f"{location}: {error.message}"


def _rank(order_seed: int, label: str) -> str:
    payload = f"{ORDER_ALGORITHM}\0{order_seed}\0{label}".encode()
    return hashlib.sha256(payload).hexdigest()


def _cell_id(subject_count: int, query_tile_size: int, source_tile_size: int) -> str:
    return (
        f"subjects-{subject_count:06d}-"
        f"tiles-q{query_tile_size:06d}-s{source_tile_size:06d}"
    )


def _condition_schedule(
    subject_counts: Sequence[int],
    tile_shapes: Sequence[tuple[int, int]],
    *,
    repeats: int,
    warmup_evaluations: int,
    order_seed: int,
) -> list[dict[str, Any]]:
    cells = [
        (count, query_tile_size, source_tile_size)
        for count in subject_counts
        for query_tile_size, source_tile_size in tile_shapes
    ]
    ordered_cells = sorted(
        cells,
        key=lambda cell: _rank(order_seed, _cell_id(*cell)),
    )
    conditions: list[dict[str, Any]] = []
    sequence = 1
    for count, query_tile_size, source_tile_size in ordered_cells:
        cell_id = _cell_id(count, query_tile_size, source_tile_size)
        strategies = sorted(
            STRATEGIES,
            key=lambda strategy: _rank(order_seed, f"{cell_id}:strategy:{strategy}"),
        )
        for strategy in strategies:
            condition_id = f"condition-{sequence:04d}-{cell_id}-{strategy}"
            output = f"conditions/{condition_id}"
            conditions.append(
                {
                    "sequence": sequence,
                    "condition_id": condition_id,
                    "cell_id": cell_id,
                    "subject_count": count,
                    "effective_pairwise_evaluation": {
                        "mode": "blockwise",
                        "query_tile_size": query_tile_size,
                        "source_tile_size": source_tile_size,
                    },
                    "tile_autograd_strategy": strategy,
                    "output_directory": output,
                    "argv": [
                        "diffeoforge",
                        "modern-benchmark",
                        CONFIG_PLACEHOLDER,
                        "--subjects",
                        str(count),
                        "--repeats",
                        str(repeats),
                        "--warmups",
                        str(warmup_evaluations),
                        "--tile-autograd-strategy",
                        strategy,
                        "--query-tile-size",
                        str(query_tile_size),
                        "--source-tile-size",
                        str(source_tile_size),
                        "--output",
                        output,
                    ],
                }
            )
            sequence += 1
    return conditions


def _validate_design(design: dict[str, Any]) -> None:
    try:
        jsonschema.Draft202012Validator(_schema()).validate(design)
    except jsonschema.ValidationError as error:
        raise ModernBenchmarkMatrixDesignError(
            "Modern benchmark matrix design schema validation failed at "
            f"{_format_schema_error(error)}"
        ) from error

    inventory = design["input"]
    if len(inventory["subjects"]) != inventory["available_subject_count"]:
        raise ModernBenchmarkMatrixDesignError(
            "Available subject count differs from the inventory"
        )
    if inventory["template"]["role"] != "template" or any(
        subject["role"] != "subject" for subject in inventory["subjects"]
    ):
        raise ModernBenchmarkMatrixDesignError("Matrix design mesh roles are inconsistent")

    protocol = design["protocol"]
    counts = protocol["subject_counts"]
    if len(counts) != len(set(counts)):
        raise ModernBenchmarkMatrixDesignError(
            "Matrix design subject counts must be unique"
        )
    if max(counts) > inventory["available_subject_count"]:
        raise ModernBenchmarkMatrixDesignError(
            "Matrix design requests more subjects than the frozen inventory provides"
        )
    shapes = [
        (shape["query_tile_size"], shape["source_tile_size"])
        for shape in protocol["tile_shapes"]
    ]
    if len(shapes) != len(set(shapes)):
        raise ModernBenchmarkMatrixDesignError("Matrix design tile shapes must be unique")
    expected_cell_count = len(counts) * len(shapes)
    expected_condition_count = expected_cell_count * len(STRATEGIES)
    if expected_condition_count > MAX_MATRIX_CONDITIONS:
        raise ModernBenchmarkMatrixDesignError(
            f"Matrix design exceeds the {MAX_MATRIX_CONDITIONS}-condition limit"
        )
    if protocol["cell_count"] != expected_cell_count:
        raise ModernBenchmarkMatrixDesignError("Matrix design cell count is inconsistent")
    if protocol["condition_count"] != expected_condition_count:
        raise ModernBenchmarkMatrixDesignError(
            "Matrix design condition count is inconsistent"
        )
    if protocol["maximum_condition_count"] != MAX_MATRIX_CONDITIONS:
        raise ModernBenchmarkMatrixDesignError(
            "Matrix design maximum condition count is inconsistent"
        )
    expected = _condition_schedule(
        counts,
        shapes,
        repeats=protocol["repeats_per_condition"],
        warmup_evaluations=protocol["warmup_evaluations_per_repeat"],
        order_seed=protocol["order_seed"],
    )
    if design["conditions"] != expected:
        raise ModernBenchmarkMatrixDesignError(
            "Matrix conditions differ from the declared deterministic full-factorial schedule"
        )


def _integer(name: str, value: int, *, minimum: int, maximum: int) -> int:
    if isinstance(value, bool) or not isinstance(value, int):
        raise TypeError(f"{name} must be an integer")
    if value < minimum or value > maximum:
        raise ValueError(f"{name} must be between {minimum} and {maximum}")
    return value


def _subject_counts(values: Sequence[int], available: int) -> list[int]:
    if isinstance(values, (str, bytes)) or not isinstance(values, Sequence) or not values:
        raise ValueError("subject_counts must be a non-empty sequence of integers")
    counts = [
        _integer("subject_count", value, minimum=1, maximum=MAX_SUBJECT_COUNT)
        for value in values
    ]
    if len(counts) != len(set(counts)):
        raise ValueError("subject_counts must not contain duplicates")
    if max(counts) > available:
        raise ConfigurationError(
            f"Matrix design requests {max(counts)} subjects but only {available} are available"
        )
    return counts


def _tile_shapes(values: Sequence[Sequence[int]]) -> list[tuple[int, int]]:
    if isinstance(values, (str, bytes)) or not isinstance(values, Sequence) or not values:
        raise ValueError("tile_shapes must be a non-empty sequence of ordered integer pairs")
    shapes: list[tuple[int, int]] = []
    for value in values:
        if isinstance(value, (str, bytes)) or not isinstance(value, Sequence) or len(value) != 2:
            raise ValueError("each tile shape must contain query and source tile sizes")
        query_tile_size = _integer(
            "query_tile_size", value[0], minimum=1, maximum=MAX_TILE_DIMENSION
        )
        source_tile_size = _integer(
            "source_tile_size", value[1], minimum=1, maximum=MAX_TILE_DIMENSION
        )
        shapes.append((query_tile_size, source_tile_size))
    if len(shapes) != len(set(shapes)):
        raise ValueError("tile_shapes must not contain duplicate ordered pairs")
    return shapes


def _timestamp(value: str | None) -> str:
    if value is None:
        return datetime.now(UTC).isoformat(timespec="seconds")
    if not isinstance(value, str) or not value:
        raise ValueError("created_at must be a non-empty string")
    return value


def _mesh_record(label: str, role: str, metadata) -> dict[str, Any]:
    return {
        "label": label,
        "role": role,
        "bytes": metadata.bytes,
        "sha256": metadata.sha256,
        "points": metadata.points,
        "triangles": metadata.cells,
    }


def collect_modern_benchmark_matrix_design(
    config_path: Path | str,
    *,
    subject_counts: Sequence[int],
    tile_shapes: Sequence[Sequence[int]],
    repeats_per_condition: int = 5,
    warmup_evaluations: int = 1,
    order_seed: int = 20260717,
    created_at: str | None = None,
) -> dict[str, Any]:
    """Freeze one full-factorial multi-tile design without measuring it."""

    repeats = _integer("repeats_per_condition", repeats_per_condition, minimum=1, maximum=100)
    warmups = _integer("warmup_evaluations", warmup_evaluations, minimum=0, maximum=20)
    seed = _integer("order_seed", order_seed, minimum=0, maximum=2**31 - 1)
    shapes = _tile_shapes(tile_shapes)
    source = Path(config_path).expanduser().resolve()
    source_sha256 = sha256_file(source)
    config = load_modern_workflow_config(source)
    source_pairwise = pairwise_evaluation_from_config(config)
    if source_pairwise.mode != "blockwise":
        raise ConfigurationError(
            "modern-benchmark-matrix-design requires configured blockwise pairwise execution"
        )
    if config["preprocessing"]["procrustes"]["enabled"]:
        raise ConfigurationError(
            "modern-benchmark-matrix-design requires preprocessing.procrustes.enabled=false"
        )
    inputs = validate_input_paths(config, source)
    template_metadata, subject_metadata = inspect_inputs(inputs)
    if sha256_file(source) != source_sha256:
        raise ModernBenchmarkMatrixDesignError(
            "Source configuration changed while the matrix design was collected"
        )
    counts = _subject_counts(subject_counts, len(inputs.subjects))
    cell_count = len(counts) * len(shapes)
    condition_count = cell_count * len(STRATEGIES)
    if condition_count > MAX_MATRIX_CONDITIONS:
        raise ValueError(
            "Full-factorial matrix would create "
            f"{condition_count} conditions, exceeding the {MAX_MATRIX_CONDITIONS}-condition limit"
        )
    template = _mesh_record(inputs.template.name, "template", template_metadata)
    subjects = [
        _mesh_record(path.name, "subject", metadata)
        for path, metadata in zip(inputs.subjects, subject_metadata, strict=True)
    ]
    deformation = config["model"]["deformation"]
    attachment = config["model"]["attachment"]
    optimizer = config["optimization"]
    design = {
        "matrix_design_version": MATRIX_DESIGN_VERSION,
        "design_id": "full_factorial_subject_tile_autograd",
        "created_at": _timestamp(created_at),
        "software": {
            "diffeoforge": __version__,
            "benchmark_version": TILE_OVERRIDE_BENCHMARK_VERSION,
        },
        "source_config": {
            "filename": source.name,
            "sha256": source_sha256,
            "project": config["project"]["name"],
        },
        "input": {
            "template": template,
            "subjects": subjects,
            "available_subject_count": len(subjects),
            "selection": "first subjects in validated deterministic path order",
        },
        "configuration": {
            "control_points": config["initialization"]["control_points"]["count"],
            "timepoints": deformation["timepoints"],
            "attachment_type": attachment["type"],
            "shooting_integrator": deformation["shooting_integrator"],
            "flow_integrator": deformation["flow_integrator"],
            "gradient_block": optimizer["block_order"][0],
            "threads": config["runtime"]["threads"],
            "random_seed": config["runtime"]["random_seed"],
            "source_pairwise_evaluation": source_pairwise.as_manifest(),
        },
        "protocol": {
            "frozen_before_results": True,
            "strategies": list(STRATEGIES),
            "subject_counts": counts,
            "tile_shapes": [
                {
                    "query_tile_size": query_tile_size,
                    "source_tile_size": source_tile_size,
                }
                for query_tile_size, source_tile_size in shapes
            ],
            "cell_count": cell_count,
            "condition_count": condition_count,
            "maximum_condition_count": MAX_MATRIX_CONDITIONS,
            "repeats_per_condition": repeats,
            "warmup_evaluations_per_repeat": warmups,
            "order_seed": seed,
            "order_algorithm": ORDER_ALGORITHM,
            "pairing": (
                "standard and recompute are adjacent within each subject-prefix and ordered "
                "query/source tile-shape cell"
            ),
            "analysis_policy": ANALYSIS_POLICY,
        },
        "conditions": _condition_schedule(
            counts,
            shapes,
            repeats=repeats,
            warmup_evaluations=warmups,
            order_seed=seed,
        ),
        "scientific_boundary": SCIENTIFIC_BOUNDARY,
    }
    _validate_design(design)
    return design


