def write_modern_benchmark_matrix_design(
    design: dict[str, Any], destination: Path | str
) -> Path:
    """Atomically publish an immutable JSON/sidecar/HTML matrix directory."""

    _validate_design(design)
    output = Path(destination).expanduser().resolve()
    if output.exists():
        raise FileExistsError(f"Modern benchmark matrix design already exists: {output}")
    output.parent.mkdir(parents=True, exist_ok=True)
    temporary = output.parent / f".{output.name}.tmp-{uuid.uuid4().hex}"
    temporary.mkdir()
    try:
        design_path = temporary / MATRIX_DESIGN_JSON_NAME
        with design_path.open("x", encoding="utf-8", newline="\n") as handle:
            json.dump(design, handle, indent=2, ensure_ascii=False, sort_keys=True)
            handle.write("\n")
        (temporary / MATRIX_DESIGN_SIDECAR_NAME).write_text(
            f"{sha256_file(design_path)}  {MATRIX_DESIGN_JSON_NAME}\n",
            encoding="utf-8",
            newline="\n",
        )
        (temporary / MATRIX_DESIGN_HTML_NAME).write_text(
            render_modern_benchmark_matrix_design_html(design),
            encoding="utf-8",
            newline="\n",
        )
        temporary.rename(output)
    except Exception:
        if temporary.exists():
            shutil.rmtree(temporary)
        raise
    return output


def verify_modern_benchmark_matrix_design(directory: Path | str) -> dict[str, Any]:
    """Verify immutable matrix-design files and return the strict JSON object."""

    root = Path(directory).expanduser().resolve()
    expected = {
        MATRIX_DESIGN_JSON_NAME,
        MATRIX_DESIGN_SIDECAR_NAME,
        MATRIX_DESIGN_HTML_NAME,
    }
    if not root.is_dir() or {path.name for path in root.iterdir()} != expected:
        raise ModernBenchmarkMatrixDesignError(
            "Benchmark matrix design directory has unexpected files"
        )
    design_path = root / MATRIX_DESIGN_JSON_NAME
    expected_sidecar = f"{sha256_file(design_path)}  {MATRIX_DESIGN_JSON_NAME}\n"
    if (root / MATRIX_DESIGN_SIDECAR_NAME).read_text(encoding="utf-8") != expected_sidecar:
        raise ModernBenchmarkMatrixDesignError(
            "Benchmark matrix design SHA-256 sidecar does not match"
        )
    try:
        design = json.loads(design_path.read_text(encoding="utf-8"))
    except json.JSONDecodeError as error:
        raise ModernBenchmarkMatrixDesignError(
            "Benchmark matrix design is not valid JSON"
        ) from error
    if not isinstance(design, dict):
        raise ModernBenchmarkMatrixDesignError(
            "Benchmark matrix design must be a JSON object"
        )
    _validate_design(design)
    observed_html = (root / MATRIX_DESIGN_HTML_NAME).read_text(encoding="utf-8")
    if observed_html != render_modern_benchmark_matrix_design_html(design):
        raise ModernBenchmarkMatrixDesignError(
            "Benchmark matrix design HTML differs from the frozen design"
        )
    return design


def default_modern_benchmark_matrix_design_path(config_path: Path | str) -> Path:
    return Path(config_path).expanduser().resolve().with_suffix(".benchmark-matrix")


def create_modern_benchmark_matrix_design(
    config_path: Path | str,
    *,
    subject_counts: Sequence[int],
    tile_shapes: Sequence[Sequence[int]],
    repeats_per_condition: int = 5,
    warmup_evaluations: int = 1,
    order_seed: int = 20260717,
    destination: Path | str | None = None,
) -> Path:
    design = collect_modern_benchmark_matrix_design(
        config_path,
        subject_counts=subject_counts,
        tile_shapes=tile_shapes,
        repeats_per_condition=repeats_per_condition,
        warmup_evaluations=warmup_evaluations,
        order_seed=order_seed,
    )
    output = (
        default_modern_benchmark_matrix_design_path(config_path)
        if destination is None
        else destination
    )
    return write_modern_benchmark_matrix_design(design, output)
