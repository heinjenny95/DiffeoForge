"""Immutable pre-results designs for multi-cycle optimizer scaling studies."""

from __future__ import annotations

import hashlib
import html
import json
import shutil
import uuid
from collections.abc import Sequence
from datetime import UTC, datetime
from importlib.resources import files
from pathlib import Path
from typing import Any

import jsonschema

from diffeoforge import __version__
from diffeoforge.config import ConfigurationError, validate_input_paths
from diffeoforge.mesh import inspect_inputs, sha256_file
from diffeoforge.modern_optimizer_benchmark import BENCHMARK_VERSION
from diffeoforge.modern_workflow import (
    load_modern_workflow_config,
    pairwise_evaluation_from_config,
)

DESIGN_VERSION = "0.1"
DESIGN_JSON_NAME = "optimizer-design.json"
DESIGN_SIDECAR_NAME = "optimizer-design.sha256"
DESIGN_HTML_NAME = "optimizer-design.html"
HTML_MARKER = (
    "<!-- Generated by DiffeoForge modern-optimizer-benchmark-design; "
    "immutable pre-results evidence. -->"
)
ORDER_ALGORITHM = "sha256-ranked-subject-cycle-cells-v0.1"
CONFIG_PLACEHOLDER = "<verified-source-config>"
MAX_SUBJECT_COUNT = 1_000
MAX_CYCLE_CAP = 100
MAX_CONDITIONS = 1_000
ANALYSIS_POLICY = (
    "Preserve every separate raw optimizer report; report descriptive time, sampled RSS, work "
    "counts, termination, and repeat consistency without selecting a preferred condition, "
    "fitting an ETA model, extrapolating beyond observed cells, or claiming convergence."
)
SCIENTIFIC_BOUNDARY = (
    "This artifact freezes a full-factorial subject-prefix and optimizer-cycle benchmark design "
    "before results exist. It performs no optimization, timing, memory sampling, convergence "
    "assessment, ETA fitting, Deformetrica comparison, or 300-subject feasibility test."
)


class ModernOptimizerBenchmarkDesignError(RuntimeError):
    """Raised when an optimizer design is invalid or cannot be published."""


def _schema() -> dict[str, Any]:
    resource = files("diffeoforge.schema").joinpath(
        "modern-optimizer-benchmark-design-v0.1.json"
    )
    return json.loads(resource.read_text(encoding="utf-8"))


def _rank(order_seed: int, label: str) -> str:
    return hashlib.sha256(f"{ORDER_ALGORITHM}\0{order_seed}\0{label}".encode()).hexdigest()


def _cell_id(subject_count: int, cycle_cap: int) -> str:
    return f"subjects-{subject_count:04d}-cycles-{cycle_cap:03d}"


def _condition_schedule(
    subject_counts: Sequence[int],
    cycle_caps: Sequence[int],
    *,
    repeats: int,
    warmup_runs: int,
    order_seed: int,
) -> list[dict[str, Any]]:
    cells = [(subjects, cycles) for subjects in subject_counts for cycles in cycle_caps]
    ordered = sorted(cells, key=lambda cell: _rank(order_seed, _cell_id(*cell)))
    conditions = []
    for sequence, (subjects, cycles) in enumerate(ordered, start=1):
        cell_id = _cell_id(subjects, cycles)
        condition_id = f"condition-{sequence:04d}-{cell_id}"
        output = f"conditions/{condition_id}"
        conditions.append(
            {
                "sequence": sequence,
                "condition_id": condition_id,
                "cell_id": cell_id,
                "subject_count": subjects,
                "cycle_cap": cycles,
                "output_directory": output,
                "argv": [
                    "diffeoforge",
                    "modern-optimizer-benchmark",
                    CONFIG_PLACEHOLDER,
                    "--subjects",
                    str(subjects),
                    "--cycles",
                    str(cycles),
                    "--repeats",
                    str(repeats),
                    "--warmups",
                    str(warmup_runs),
                    "--output",
                    output,
                ],
            }
        )
    return conditions


def _validate_design(design: dict[str, Any]) -> None:
    try:
        jsonschema.Draft202012Validator(_schema()).validate(design)
    except jsonschema.ValidationError as error:
        location = ".".join(str(part) for part in error.absolute_path) or "document"
        raise ModernOptimizerBenchmarkDesignError(
            f"Optimizer benchmark design schema validation failed at {location}: "
            f"{error.message}"
        ) from error

    inventory = design["input"]
    if len(inventory["subjects"]) != inventory["available_subject_count"]:
        raise ModernOptimizerBenchmarkDesignError(
            "Available subject count differs from the optimizer-design inventory"
        )
    if inventory["template"]["role"] != "template" or any(
        subject["role"] != "subject" for subject in inventory["subjects"]
    ):
        raise ModernOptimizerBenchmarkDesignError("Optimizer-design mesh roles are inconsistent")

    protocol = design["protocol"]
    subjects = protocol["subject_counts"]
    cycles = protocol["cycle_caps"]
    if len(subjects) != len(set(subjects)) or len(cycles) != len(set(cycles)):
        raise ModernOptimizerBenchmarkDesignError("Optimizer-design factor levels must be unique")
    if max(subjects) > inventory["available_subject_count"]:
        raise ModernOptimizerBenchmarkDesignError(
            "Optimizer design requests more subjects than its frozen inventory provides"
        )
    expected_count = len(subjects) * len(cycles)
    if protocol["condition_count"] != expected_count:
        raise ModernOptimizerBenchmarkDesignError(
            "Optimizer-design condition count is inconsistent"
        )
    if protocol["maximum_condition_count"] != MAX_CONDITIONS:
        raise ModernOptimizerBenchmarkDesignError(
            "Optimizer-design maximum condition count is inconsistent"
        )
    expected = _condition_schedule(
        subjects,
        cycles,
        repeats=protocol["repeats_per_condition"],
        warmup_runs=protocol["warmup_runs_per_repeat"],
        order_seed=protocol["order_seed"],
    )
    if design["conditions"] != expected:
        raise ModernOptimizerBenchmarkDesignError(
            "Optimizer conditions differ from the declared deterministic full-factorial schedule"
        )


def _integer(name: str, value: int, *, minimum: int, maximum: int) -> int:
    if isinstance(value, bool) or not isinstance(value, int):
        raise TypeError(f"{name} must be an integer")
    if value < minimum or value > maximum:
        raise ValueError(f"{name} must be between {minimum} and {maximum}")
    return value


def _levels(name: str, values: Sequence[int], *, maximum: int) -> list[int]:
    if isinstance(values, (str, bytes)) or not isinstance(values, Sequence) or not values:
        raise ValueError(f"{name} must be a non-empty sequence of integers")
    normalized = [
        _integer(name[:-1], value, minimum=1, maximum=maximum) for value in values
    ]
    if len(normalized) != len(set(normalized)):
        raise ValueError(f"{name} must not contain duplicates")
    return normalized


def _timestamp(value: str | None) -> str:
    if value is None:
        return datetime.now(UTC).isoformat(timespec="seconds")
    if not isinstance(value, str) or not value:
        raise ValueError("created_at must be a non-empty string")
    return value


def _mesh_record(label: str, role: str, metadata) -> dict[str, Any]:
    return {
        "label": label,
        "role": role,
        "bytes": metadata.bytes,
        "sha256": metadata.sha256,
        "points": metadata.points,
        "triangles": metadata.cells,
    }


def collect_modern_optimizer_benchmark_design(
    config_path: Path | str,
    *,
    subject_counts: Sequence[int],
    cycle_caps: Sequence[int],
    repeats_per_condition: int = 3,
    warmup_runs: int = 0,
    order_seed: int = 20260722,
    created_at: str | None = None,
) -> dict[str, Any]:
    """Freeze one subject-prefix by cycle-cap design without running it."""

    repeats = _integer("repeats_per_condition", repeats_per_condition, minimum=1, maximum=50)
    warmups = _integer("warmup_runs", warmup_runs, minimum=0, maximum=10)
    seed = _integer("order_seed", order_seed, minimum=0, maximum=2**31 - 1)
    source = Path(config_path).expanduser().resolve()
    source_sha256 = sha256_file(source)
    config = load_modern_workflow_config(source)
    if config["preprocessing"]["procrustes"]["enabled"]:
        raise ConfigurationError(
            "modern-optimizer-benchmark-design requires preprocessing.procrustes.enabled=false"
        )
    inputs = validate_input_paths(config, source)
    template_metadata, subject_metadata = inspect_inputs(inputs)
    if sha256_file(source) != source_sha256:
        raise ModernOptimizerBenchmarkDesignError(
            "Source configuration changed while the optimizer design was collected"
        )
    subjects = _levels("subject_counts", subject_counts, maximum=MAX_SUBJECT_COUNT)
    cycles = _levels("cycle_caps", cycle_caps, maximum=MAX_CYCLE_CAP)
    if max(subjects) > len(inputs.subjects):
        raise ConfigurationError(
            f"Optimizer design requests {max(subjects)} subjects but only "
            f"{len(inputs.subjects)} are available"
        )
    condition_count = len(subjects) * len(cycles)
    if condition_count > MAX_CONDITIONS:
        raise ValueError(
            f"Full-factorial optimizer design would create {condition_count} conditions, "
            f"exceeding the {MAX_CONDITIONS}-condition limit"
        )

    template = _mesh_record(inputs.template.name, "template", template_metadata)
    subject_records = [
        _mesh_record(path.name, "subject", metadata)
        for path, metadata in zip(inputs.subjects, subject_metadata, strict=True)
    ]
    deformation = config["model"]["deformation"]
    attachment = config["model"]["attachment"]
    optimizer = config["optimization"]
    design = {
        "optimizer_design_version": DESIGN_VERSION,
        "design_id": "full_factorial_subject_cycle",
        "created_at": _timestamp(created_at),
        "software": {
            "diffeoforge": __version__,
            "optimizer_benchmark_version": BENCHMARK_VERSION,
        },
        "source_config": {
            "filename": source.name,
            "sha256": source_sha256,
            "project": config["project"]["name"],
        },
        "input": {
            "template": template,
            "subjects": subject_records,
            "available_subject_count": len(subject_records),
            "selection": "first subjects in validated deterministic path order",
        },
        "configuration": {
            "control_points": config["initialization"]["control_points"]["count"],
            "timepoints": deformation["timepoints"],
            "attachment_type": attachment["type"],
            "shooting_integrator": deformation["shooting_integrator"],
            "flow_integrator": deformation["flow_integrator"],
            "pairwise_evaluation": pairwise_evaluation_from_config(config).as_manifest(),
            "source_max_cycles": optimizer["max_cycles"],
            "block_order": list(optimizer["block_order"]),
            "momenta_step_size": optimizer["momenta_step_size"],
            "template_step_size": optimizer["template_step_size"],
            "control_points_step_size": optimizer["control_points_step_size"],
            "backtracking_factor": optimizer["backtracking_factor"],
            "armijo_constant": optimizer["armijo_constant"],
            "gradient_tolerance": optimizer["gradient_tolerance"],
            "minimum_step_size": optimizer["minimum_step_size"],
            "max_line_search_iterations": optimizer["max_line_search_iterations"],
            "threads": config["runtime"]["threads"],
            "random_seed": config["runtime"]["random_seed"],
        },
        "protocol": {
            "frozen_before_results": True,
            "subject_counts": subjects,
            "cycle_caps": cycles,
            "condition_count": condition_count,
            "maximum_condition_count": MAX_CONDITIONS,
            "repeats_per_condition": repeats,
            "warmup_runs_per_repeat": warmups,
            "order_seed": seed,
            "order_algorithm": ORDER_ALGORITHM,
            "analysis_policy": ANALYSIS_POLICY,
        },
        "conditions": _condition_schedule(
            subjects,
            cycles,
            repeats=repeats,
            warmup_runs=warmups,
            order_seed=seed,
        ),
        "scientific_boundary": SCIENTIFIC_BOUNDARY,
    }
    _validate_design(design)
    return design


