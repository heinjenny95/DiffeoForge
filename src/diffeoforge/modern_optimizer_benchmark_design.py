def write_modern_optimizer_benchmark_design(
    design: dict[str, Any], destination: Path | str
) -> Path:
    """Atomically publish an immutable JSON/sidecar/HTML design directory."""

    _validate_design(design)
    output = Path(destination).expanduser().resolve()
    if output.exists():
        raise FileExistsError(f"Modern optimizer benchmark design already exists: {output}")
    output.parent.mkdir(parents=True, exist_ok=True)
    temporary = output.parent / f".{output.name}.tmp-{uuid.uuid4().hex}"
    temporary.mkdir()
    try:
        design_path = temporary / DESIGN_JSON_NAME
        with design_path.open("x", encoding="utf-8", newline="\n") as handle:
            json.dump(design, handle, indent=2, ensure_ascii=False, sort_keys=True)
            handle.write("\n")
        (temporary / DESIGN_SIDECAR_NAME).write_text(
            f"{sha256_file(design_path)}  {DESIGN_JSON_NAME}\n",
            encoding="utf-8",
            newline="\n",
        )
        (temporary / DESIGN_HTML_NAME).write_text(
            render_modern_optimizer_benchmark_design_html(design),
            encoding="utf-8",
            newline="\n",
        )
        temporary.rename(output)
    except Exception:
        if temporary.exists():
            shutil.rmtree(temporary)
        raise
    return output


def verify_modern_optimizer_benchmark_design(directory: Path | str) -> dict[str, Any]:
    """Verify immutable optimizer-design files and return the strict JSON object."""

    root = Path(directory).expanduser().resolve()
    expected = {DESIGN_JSON_NAME, DESIGN_SIDECAR_NAME, DESIGN_HTML_NAME}
    if not root.is_dir() or {path.name for path in root.iterdir()} != expected:
        raise ModernOptimizerBenchmarkDesignError(
            "Optimizer benchmark design directory has unexpected files"
        )
    design_path = root / DESIGN_JSON_NAME
    expected_sidecar = f"{sha256_file(design_path)}  {DESIGN_JSON_NAME}\n"
    if (root / DESIGN_SIDECAR_NAME).read_text(encoding="utf-8") != expected_sidecar:
        raise ModernOptimizerBenchmarkDesignError(
            "Optimizer benchmark design SHA-256 sidecar does not match"
        )
    try:
        design = json.loads(design_path.read_text(encoding="utf-8"))
    except json.JSONDecodeError as error:
        raise ModernOptimizerBenchmarkDesignError(
            "Optimizer benchmark design is not valid JSON"
        ) from error
    if not isinstance(design, dict):
        raise ModernOptimizerBenchmarkDesignError(
            "Optimizer benchmark design must be a JSON object"
        )
    _validate_design(design)
    if (root / DESIGN_HTML_NAME).read_text(
        encoding="utf-8"
    ) != render_modern_optimizer_benchmark_design_html(design):
        raise ModernOptimizerBenchmarkDesignError(
            "Optimizer benchmark design HTML differs from the frozen design"
        )
    return design


def default_modern_optimizer_benchmark_design_path(config_path: Path | str) -> Path:
    return Path(config_path).expanduser().resolve().with_suffix(".optimizer-study")


def create_modern_optimizer_benchmark_design(
    config_path: Path | str,
    *,
    subject_counts: Sequence[int],
    cycle_caps: Sequence[int],
    repeats_per_condition: int = 3,
    warmup_runs: int = 0,
    order_seed: int = 20260722,
    destination: Path | str | None = None,
) -> Path:
    design = collect_modern_optimizer_benchmark_design(
        config_path,
        subject_counts=subject_counts,
        cycle_caps=cycle_caps,
        repeats_per_condition=repeats_per_condition,
        warmup_runs=warmup_runs,
        order_seed=order_seed,
    )
    output = (
        default_modern_optimizer_benchmark_design_path(config_path)
        if destination is None
        else destination
    )
    return write_modern_optimizer_benchmark_design(design, output)
