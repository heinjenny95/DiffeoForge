class ModernOptimizerBenchmarkError(RuntimeError):
    """Raised when optimizer evidence cannot be measured or published safely."""


def _schema() -> dict[str, Any]:
    resource = files("diffeoforge.schema").joinpath(SCHEMA_NAME)
    return json.loads(resource.read_text(encoding="utf-8"))


def _tensor_sha256(tensor: torch.Tensor) -> str:
    value = tensor.detach().cpu().contiguous()
    digest = hashlib.sha256()
    digest.update(str(value.dtype).encode("ascii"))
    digest.update(json.dumps(list(value.shape), separators=(",", ":")).encode("ascii"))
    digest.update(value.numpy().tobytes(order="C"))
    return digest.hexdigest()


def _history_sha256(result: AtlasOptimizationResult) -> str:
    payload = [asdict(record) for record in result.history]
    serialized = json.dumps(
        payload,
        allow_nan=False,
        ensure_ascii=False,
        separators=(",", ":"),
        sort_keys=True,
    ).encode("utf-8")
    return hashlib.sha256(serialized).hexdigest()


def _optimizer_keywords(config: dict[str, Any], max_cycles: int) -> dict[str, Any]:
    model = config["model"]
    deformation = model["deformation"]
    attachment = model["attachment"]
    optimizer = config["optimization"]
    pairwise = pairwise_evaluation_from_config(config)
    return {
        "deformation_kernel_width": deformation["kernel_width"],
        "attachment_kernel_width": attachment["kernel_width"],
        "noise_variance": model["noise_variance"],
        "number_of_time_points": deformation["timepoints"],
        "attachment_type": attachment["type"],
        "shooting_integrator": deformation["shooting_integrator"],
        "flow_integrator": deformation["flow_integrator"],
        "gaussian_tile_plan": pairwise.gaussian_tile_plan,
        "max_cycles": max_cycles,
        "block_order": optimizer["block_order"],
        "momenta_step_size": optimizer["momenta_step_size"],
        "template_step_size": optimizer["template_step_size"],
        "control_points_step_size": optimizer["control_points_step_size"],
        "backtracking_factor": optimizer["backtracking_factor"],
        "armijo_constant": optimizer["armijo_constant"],
        "gradient_tolerance": optimizer["gradient_tolerance"],
        "minimum_step_size": optimizer["minimum_step_size"],
        "max_line_search_iterations": optimizer["max_line_search_iterations"],
    }


def _prepare_targets(
    config: dict[str, Any],
    targets: tuple[tuple[torch.Tensor, torch.Tensor], ...],
) -> tuple[Any, ...]:
    attachment = config["model"]["attachment"]
    tile_plan = pairwise_evaluation_from_config(config).gaussian_tile_plan
    return tuple(
        prepare_surface_attachment_target(
            vertices,
            triangles,
            attachment["kernel_width"],
            attachment_type=attachment["type"],
            gaussian_tile_plan=tile_plan,
        )
        for vertices, triangles in targets
    )


def _run_optimizer(
    config: dict[str, Any],
    problem: tuple[Any, ...],
    prepared_targets: tuple[Any, ...],
    max_cycles: int,
) -> AtlasOptimizationResult:
    template, triangles, targets, controls, momenta = problem
    return optimize_atlas(
        template,
        triangles,
        targets,
        controls,
        momenta,
        prepared_targets=prepared_targets,
        **_optimizer_keywords(config, max_cycles),
    )


def _result_sample(result: AtlasOptimizationResult) -> dict[str, Any]:
    decisions = result.history[1:]
    accepted = sum(record.status == "accepted" for record in decisions)
    stationary = sum(record.status == "stationary" for record in decisions)
    failed = sum(record.status == "failed" for record in decisions)
    final = result.history[-1]
    without_gradient = result.total_line_search_evaluations - result.candidate_gradient_evaluations
    if without_gradient < 0:
        raise ModernOptimizerBenchmarkError(
            "Candidate-gradient count exceeds line-search evaluations"
        )
    return {
        "termination_reason": result.termination_reason,
        "converged": result.converged,
        "failed_block": result.failed_block,
        "cycles_completed": result.cycles_completed,
        "accepted_decisions": accepted,
        "stationary_decisions": stationary,
        "failed_decisions": failed,
        "line_search_evaluations": result.total_line_search_evaluations,
        "objective_evaluations": result.objective_evaluations,
        "gradient_evaluations": result.gradient_evaluations,
        "candidate_gradient_evaluations": result.candidate_gradient_evaluations,
        "line_search_candidates_without_gradient": without_gradient,
        "final_objective": final.objective,
        "final_attachment": final.attachment,
        "final_regularity": final.regularity,
        "history_sha256": _history_sha256(result),
        "template_sha256": _tensor_sha256(result.template_vertices),
        "control_points_sha256": _tensor_sha256(result.control_points),
        "momenta_sha256": _tensor_sha256(result.momenta),
    }


def _measure_once(
    config_path: str,
    subject_count: int,
    max_cycles: int,
    warmup_runs: int,
) -> dict[str, Any]:
    config, problem = _prepare_problem(config_path, subject_count)
    torch.set_num_threads(config["runtime"]["threads"])
    if torch.get_num_threads() != config["runtime"]["threads"]:
        raise ModernOptimizerBenchmarkError("PyTorch did not apply the requested CPU threads")
    torch.manual_seed(config["runtime"]["random_seed"])

    targets = problem[2]
    preparation_started = time.perf_counter_ns()
    prepared_targets = _prepare_targets(config, targets)
    preparation_elapsed = time.perf_counter_ns() - preparation_started
    for _ in range(warmup_runs):
        _run_optimizer(config, problem, prepared_targets, max_cycles)
    gc.collect()

    process = psutil.Process()
    baseline_rss = process.memory_info().rss
    rss_samples = [baseline_rss]
    stop = threading.Event()

    def sample_rss() -> None:
        while not stop.wait(RSS_SAMPLING_INTERVAL_SECONDS):
            rss_samples.append(process.memory_info().rss)

    sampler = threading.Thread(
        target=sample_rss,
        name="diffeoforge-optimizer-rss-sampler",
        daemon=True,
    )
    sampler.start()
    started = time.perf_counter_ns()
    try:
        result = _run_optimizer(config, problem, prepared_targets, max_cycles)
    finally:
        elapsed = time.perf_counter_ns() - started
        rss_samples.append(process.memory_info().rss)
        stop.set()
        sampler.join()
    peak_rss = max(rss_samples)
    return {
        "target_preparation_wall_time_ns": preparation_elapsed,
        "optimizer_wall_time_ns": elapsed,
        "rss_before_bytes": baseline_rss,
        "sampled_peak_rss_bytes": peak_rss,
        "sampled_rss_delta_bytes": max(0, peak_rss - baseline_rss),
        "rss_sample_count": len(rss_samples),
        **_result_sample(result),
    }


def _benchmark_worker(
    connection,
    config_path: str,
    subject_count: int,
    max_cycles: int,
    warmup_runs: int,
) -> None:
    try:
        connection.send(
            {
                "ok": True,
                "sample": _measure_once(
                    config_path,
                    subject_count,
                    max_cycles,
                    warmup_runs,
                ),
            }
        )
    except Exception as error:
        connection.send(
            {
                "ok": False,
                "error_type": type(error).__name__,
                "message": str(error),
            }
        )
    finally:
        connection.close()


def _run_fresh_sample(
    config_path: Path,
    subject_count: int,
    max_cycles: int,
    warmup_runs: int,
) -> dict[str, Any]:
    context = get_context("spawn")
    receiver, sender = context.Pipe(duplex=False)
    process = context.Process(
        target=_benchmark_worker,
        args=(sender, str(config_path), subject_count, max_cycles, warmup_runs),
        name="diffeoforge-modern-optimizer-benchmark",
    )
    process.start()
    sender.close()
    try:
        payload = receiver.recv()
    except EOFError as error:
        process.join()
        raise ModernOptimizerBenchmarkError(
            "Optimizer benchmark worker exited without returning a measurement"
        ) from error
    except BaseException:
        if process.is_alive():
            process.terminate()
        process.join()
        raise
    finally:
        receiver.close()
    process.join()
    if process.exitcode != 0:
        raise ModernOptimizerBenchmarkError(
            f"Optimizer benchmark worker exited with code {process.exitcode}"
        )
    if not payload.get("ok"):
        raise ModernOptimizerBenchmarkError(
            f"Optimizer benchmark worker failed ({payload.get('error_type', 'Error')}): "
            f"{payload.get('message', 'no message')}"
        )
    sample = payload.get("sample")
    if not isinstance(sample, dict):
        raise ModernOptimizerBenchmarkError("Optimizer benchmark worker returned invalid data")
    return sample


def _consistency(samples: list[dict[str, Any]]) -> dict[str, Any]:
    float_fields = ("final_objective", "final_attachment", "final_regularity")
    exact_fields = (
        "termination_reason",
        "converged",
        "failed_block",
        "cycles_completed",
        "accepted_decisions",
        "stationary_decisions",
        "failed_decisions",
        "line_search_evaluations",
        "objective_evaluations",
        "gradient_evaluations",
        "candidate_gradient_evaluations",
        "line_search_candidates_without_gradient",
        "history_sha256",
        "template_sha256",
        "control_points_sha256",
        "momenta_sha256",
    )
    reference = samples[0]
    exact = all(
        all(sample[field] == reference[field] for field in exact_fields) for sample in samples[1:]
    )
    numerical = all(
        math.isclose(
            sample[field],
            reference[field],
            rel_tol=1e-12,
            abs_tol=1e-12,
        )
        for sample in samples[1:]
        for field in float_fields
    )
    return {
        "relative_tolerance": 1e-12,
        "absolute_tolerance": 1e-12,
        "exact_discrete_and_hash_match": exact,
        "numerical_match": numerical,
        "consistent": exact and numerical,
    }


def _validate_report(report: dict[str, Any]) -> None:
    try:
        jsonschema.Draft202012Validator(_schema()).validate(report)
    except jsonschema.ValidationError as error:
        location = ".".join(str(part) for part in error.absolute_path) or "document"
        raise ModernOptimizerBenchmarkError(
            f"Optimizer benchmark schema validation failed at {location}: {error.message}"
        ) from error
    samples = report["samples"]
    if len(samples) != report["configuration"]["repeats"]:
        raise ModernOptimizerBenchmarkError("Sample count differs from configured repeats")
    if [sample["repeat"] for sample in samples] != list(range(1, len(samples) + 1)):
        raise ModernOptimizerBenchmarkError("Repeat numbers must be contiguous and one-based")
    if len(report["input"]["subjects"]) != report["input"]["selected_subject_count"]:
        raise ModernOptimizerBenchmarkError("Subject inventory differs from selected count")
    for sample in samples:
        expected_delta = max(0, sample["sampled_peak_rss_bytes"] - sample["rss_before_bytes"])
        if sample["sampled_rss_delta_bytes"] != expected_delta:
            raise ModernOptimizerBenchmarkError("Sampled RSS delta is inconsistent")
        expected_without = (
            sample["line_search_evaluations"] - sample["candidate_gradient_evaluations"]
        )
        if sample["line_search_candidates_without_gradient"] != expected_without:
            raise ModernOptimizerBenchmarkError("Deferred-gradient count is inconsistent")
        if sample["objective_evaluations"] < sample["gradient_evaluations"]:
            raise ModernOptimizerBenchmarkError("Gradient count exceeds objective count")
        decisions = (
            sample["accepted_decisions"]
            + sample["stationary_decisions"]
            + sample["failed_decisions"]
        )
        if sample["objective_evaluations"] != (decisions + sample["line_search_evaluations"]):
            raise ModernOptimizerBenchmarkError("Objective-evaluation count is inconsistent")
        if sample["gradient_evaluations"] != (decisions + sample["candidate_gradient_evaluations"]):
            raise ModernOptimizerBenchmarkError("Gradient-evaluation count is inconsistent")
        if decisions > report["configuration"]["measured_max_cycles"] * 3:
            raise ModernOptimizerBenchmarkError("Optimizer decision count exceeds cycle scope")
        if sample["cycles_completed"] > report["configuration"]["measured_max_cycles"]:
            raise ModernOptimizerBenchmarkError("Completed cycles exceed benchmark scope")
        if not all(
            math.isfinite(sample[field])
            for field in ("final_objective", "final_attachment", "final_regularity")
        ):
            raise ModernOptimizerBenchmarkError("Final optimizer values must be finite")
    expected_summary = {
        field: _summary(samples, field)
        for field in (
            "target_preparation_wall_time_ns",
            "optimizer_wall_time_ns",
            "rss_before_bytes",
            "sampled_peak_rss_bytes",
            "sampled_rss_delta_bytes",
        )
    }
    if report["summary"] != expected_summary:
        raise ModernOptimizerBenchmarkError("Descriptive summary is inconsistent")
    if report["repeat_consistency"] != _consistency(samples):
        raise ModernOptimizerBenchmarkError("Repeat consistency record is inconsistent")
    if report["input"]["template"]["role"] != "template" or any(
        subject["role"] != "subject" for subject in report["input"]["subjects"]
    ):
        raise ModernOptimizerBenchmarkError("Benchmark mesh roles are inconsistent")
    largest = report["operation_model"]["largest_logical_pair"]
    if (
        largest["pair_elements"] != largest["rows"] * largest["columns"]
        or largest["float64_matrix_bytes"] != largest["pair_elements"] * 8
        or largest["float64_xyz_difference_tensor_bytes"] != largest["pair_elements"] * 24
    ):
        raise ModernOptimizerBenchmarkError("Largest logical-pair payload is inconsistent")
    tile = report["operation_model"]["largest_execution_tile"]
    if (
        tile["tile_rows"] > tile["logical_rows"]
        or tile["tile_columns"] > tile["logical_columns"]
        or tile["pair_elements"] != tile["tile_rows"] * tile["tile_columns"]
        or tile["float64_matrix_bytes"] != tile["pair_elements"] * 8
        or tile["float64_xyz_difference_tensor_bytes"] != tile["pair_elements"] * 24
    ):
        raise ModernOptimizerBenchmarkError("Largest execution-tile payload is inconsistent")


def collect_modern_optimizer_benchmark(
    config_path: Path | str,
    *,
    subject_count: int,
    max_cycles: int,
    repeats: int = 3,
    warmup_runs: int = 0,
    created_at: str | None = None,
) -> dict[str, Any]:
    """Collect fresh-process production-optimizer observations without extrapolation."""

    selected_count = _positive_integer("subject_count", subject_count, minimum=1, maximum=1000)
    cycle_count = _positive_integer("max_cycles", max_cycles, minimum=1, maximum=100)
    repeat_count = _positive_integer("repeats", repeats, minimum=1, maximum=50)
    warmups = _positive_integer("warmup_runs", warmup_runs, minimum=0, maximum=10)
    source = Path(config_path).expanduser().resolve()
    config = load_modern_workflow_config(source)
    if config["preprocessing"]["procrustes"]["enabled"]:
        raise ConfigurationError(
            "modern-optimizer-benchmark requires preprocessing.procrustes.enabled=false"
        )
    inputs = validate_input_paths(config, source)
    if selected_count > len(inputs.subjects):
        raise ConfigurationError(
            f"Requested {selected_count} benchmark subjects, but only "
            f"{len(inputs.subjects)} are available"
        )
    selected_paths = inputs.subjects[:selected_count]
    template = _mesh_record(inputs.template.name, "template", inspect_vtk(inputs.template))
    subjects = [_mesh_record(path.name, "subject", inspect_vtk(path)) for path in selected_paths]
    pairwise = pairwise_evaluation_from_config(config)
    operation = _operation_model(config, template, subjects)
    samples = [
        {
            "repeat": repeat,
            **_run_fresh_sample(source, selected_count, cycle_count, warmups),
        }
        for repeat in range(1, repeat_count + 1)
    ]
    consistency = _consistency(samples)
    warnings = [
        "Sampled process RSS can miss peaks shorter than the 5 ms sampling interval.",
        "The configured subject prefix and cycle override are benchmark scope, not convergence.",
        "Do not extrapolate these observations to 300 subjects without a prospective design.",
    ]
    if not consistency["consistent"]:
        warnings.append("Fresh-process optimizer results were not repeat-consistent.")
    optimizer = config["optimization"]
    report = {
        "benchmark_version": BENCHMARK_VERSION,
        "benchmark_id": "production_multi_cycle_optimizer",
        "created_at": _timestamp(created_at),
        "source_config": {
            "filename": source.name,
            "sha256": sha256_file(source),
            "project": config["project"]["name"],
        },
        "input": {
            "template": template,
            "subjects": subjects,
            "available_subject_count": len(inputs.subjects),
            "selected_subject_count": selected_count,
            "selection": "first subjects in validated deterministic path order",
        },
        "configuration": {
            "source_max_cycles": optimizer["max_cycles"],
            "measured_max_cycles": cycle_count,
            "block_order": optimizer["block_order"],
            "momenta_step_size": optimizer["momenta_step_size"],
            "template_step_size": optimizer["template_step_size"],
            "control_points_step_size": optimizer["control_points_step_size"],
            "backtracking_factor": optimizer["backtracking_factor"],
            "armijo_constant": optimizer["armijo_constant"],
            "gradient_tolerance": optimizer["gradient_tolerance"],
            "minimum_step_size": optimizer["minimum_step_size"],
            "max_line_search_iterations": optimizer["max_line_search_iterations"],
            "threads": config["runtime"]["threads"],
            "random_seed": config["runtime"]["random_seed"],
            "warmup_runs_per_repeat": warmups,
            "repeats": repeat_count,
            "rss_sampling_interval_ms": RSS_SAMPLING_INTERVAL_SECONDS * 1000,
            "process_isolation": "fresh multiprocessing spawn process per repeat",
            "pairwise_evaluation": pairwise.as_manifest(),
            "prepared_target_cache": (
                "target geometry and target self-term prepared once per fresh process"
            ),
        },
        "environment": {
            "diffeoforge": __version__,
            "python": platform.python_version(),
            "numpy": np.__version__,
            "pytorch": torch.__version__,
            "psutil": psutil.__version__,
            "platform": platform.platform(),
            "logical_cpus": os.cpu_count(),
            "physical_memory_bytes": _physical_memory_bytes(),
            "parent_executable": Path(sys.executable).name,
        },
        "operation_model": {
            "gaussian_calls_per_objective_forward": operation["one_objective_forward"][
                "gaussian_calls"
            ],
            "gaussian_pair_elements_per_objective_forward": operation["one_objective_forward"][
                "gaussian_pair_elements"
            ],
            "largest_logical_pair": operation["largest_logical_pair"],
            "largest_execution_tile": operation["largest_execution_tile"],
        },
        "samples": samples,
        "summary": {
            field: _summary(samples, field)
            for field in (
                "target_preparation_wall_time_ns",
                "optimizer_wall_time_ns",
                "rss_before_bytes",
                "sampled_peak_rss_bytes",
                "sampled_rss_delta_bytes",
            )
        },
        "repeat_consistency": consistency,
        "warnings": warnings,
        "scientific_boundary": SCIENTIFIC_BOUNDARY,
    }
    _validate_report(report)
    return report


def _format_bytes(value: int | float) -> str:
    return f"{value / 1024**2:.2f} MiB"


def _csv_text(value: Any) -> str:
    return "" if value is None else str(value)


