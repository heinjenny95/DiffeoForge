def verify_modern_optimizer_benchmark_report(directory: Path | str) -> dict[str, Any]:
    """Strictly verify JSON, raw CSV, and regenerated HTML."""

    root = Path(directory).expanduser().resolve()
    expected = {REPORT_JSON_NAME, REPORT_CSV_NAME, REPORT_HTML_NAME}
    if not root.is_dir() or {path.name for path in root.iterdir()} != expected:
        raise ModernOptimizerBenchmarkError("Optimizer benchmark directory has unexpected files")
    try:
        report = json.loads((root / REPORT_JSON_NAME).read_text(encoding="utf-8"))
    except json.JSONDecodeError as error:
        raise ModernOptimizerBenchmarkError("Optimizer benchmark is not valid JSON") from error
    if not isinstance(report, dict):
        raise ModernOptimizerBenchmarkError("Optimizer benchmark must be a JSON object")
    _validate_report(report)
    with (root / REPORT_CSV_NAME).open(encoding="utf-8", newline="") as handle:
        reader = csv.DictReader(handle)
        if tuple(reader.fieldnames or ()) != REPORT_COLUMNS:
            raise ModernOptimizerBenchmarkError("Optimizer benchmark CSV columns differ")
        rows = list(reader)
    expected_rows = [
        {column: _csv_text(sample[column]) for column in REPORT_COLUMNS}
        for sample in report["samples"]
    ]
    if rows != expected_rows:
        raise ModernOptimizerBenchmarkError("Optimizer benchmark CSV rows differ from JSON")
    observed_html = (root / REPORT_HTML_NAME).read_text(encoding="utf-8")
    if observed_html != render_modern_optimizer_benchmark_html(report):
        raise ModernOptimizerBenchmarkError("Optimizer benchmark HTML differs from JSON")
    return report


def _is_generated_report(directory: Path) -> bool:
    try:
        verify_modern_optimizer_benchmark_report(directory)
    except (ModernOptimizerBenchmarkError, OSError, UnicodeError):
        return False
    return True


def write_modern_optimizer_benchmark_report(
    report: dict[str, Any],
    destination: Path | str,
    *,
    overwrite: bool = False,
) -> Path:
    """Atomically publish the strict optimizer benchmark report."""

    output = Path(destination).expanduser().resolve()
    _validate_report(report)
    if output.exists() and not overwrite:
        raise FileExistsError(f"Modern optimizer benchmark already exists: {output}")
    if output.exists() and not _is_generated_report(output):
        raise ModernOptimizerBenchmarkError(
            f"Refusing to replace a directory not recognized as an optimizer benchmark: {output}"
        )
    output.parent.mkdir(parents=True, exist_ok=True)
    temporary = output.parent / f".{output.name}.tmp-{uuid.uuid4().hex}"
    backup = output.parent / f".{output.name}.backup-{uuid.uuid4().hex}"
    temporary.mkdir()
    try:
        with (temporary / REPORT_JSON_NAME).open("x", encoding="utf-8", newline="\n") as handle:
            json.dump(report, handle, indent=2, ensure_ascii=False, sort_keys=True)
            handle.write("\n")
        with (temporary / REPORT_CSV_NAME).open("x", encoding="utf-8", newline="") as handle:
            writer = csv.writer(handle, lineterminator="\n")
            writer.writerow(REPORT_COLUMNS)
            for sample in report["samples"]:
                writer.writerow(sample[column] for column in REPORT_COLUMNS)
        (temporary / REPORT_HTML_NAME).write_text(
            render_modern_optimizer_benchmark_html(report),
            encoding="utf-8",
            newline="\n",
        )
        if output.exists():
            output.rename(backup)
        try:
            temporary.rename(output)
        except Exception:
            if backup.exists() and not output.exists():
                backup.rename(output)
            raise
        if backup.exists():
            shutil.rmtree(backup, ignore_errors=True)
    except Exception:
        if temporary.exists():
            shutil.rmtree(temporary)
        raise
    return output


def default_modern_optimizer_benchmark_path(config_path: Path | str) -> Path:
    return Path(config_path).expanduser().resolve().with_suffix(".optimizer-benchmark")


def benchmark_modern_optimizer(
    config_path: Path | str,
    *,
    subject_count: int,
    max_cycles: int,
    repeats: int = 3,
    warmup_runs: int = 0,
    destination: Path | str | None = None,
    overwrite: bool = False,
) -> Path:
    report = collect_modern_optimizer_benchmark(
        config_path,
        subject_count=subject_count,
        max_cycles=max_cycles,
        repeats=repeats,
        warmup_runs=warmup_runs,
    )
    output = (
        default_modern_optimizer_benchmark_path(config_path) if destination is None else destination
    )
    return write_modern_optimizer_benchmark_report(report, output, overwrite=overwrite)
